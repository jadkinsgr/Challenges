import random
# random integer between bounds
random_integer = random.randint(1,500)
print(random_integer)

# create a random float between 0 and 1 not including the ends
random_float = random.random()
print(random_float)


# create a random float and multiply it by X to get it larger than one
random_float = random.random()*10
print(random_float)

